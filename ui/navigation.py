def page_groups_for_story(
    *,
    project_name: str | None,
    planning_pages: list[str],
) -> dict[str, list[str]]:
    if not project_name:
        return {"配置": ["模型配置"]}

    groups = {group: list(pages) for group, pages in ADVANCED_PAGE_GROUPS.items()}
    groups["规划"] = list(planning_pages)
    return groups
